    private void ActualizarDatos(String query){
        mysql cn = new mysql();
        ResultSet rs = cn.ejecutar(query);
        cn.desconectar();
        try {
            if(rs.next()){
               JOptionPane.showMessageDialog(null, rs.getString("mensaje"));
                buscarRegistros("");
                limpiar();
            }else{
                JOptionPane.showMessageDialog(null,"Problemas con el servidor!");
            }
        } catch (SQLException ex) {
            Logger.getLogger(tipos.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
