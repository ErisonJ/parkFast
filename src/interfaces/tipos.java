    private void limpiar(){
        switch(jtabpanel){
            case 0:
                codigoTU.setText("0");
                TuDescripcion.setText("");
                TuDescripcion.setBorder(codigoTU.getBorder());
                tuStatus.setSelected(true);
                break;
            case 1:
                codigoTP.setText("0");
                TpDescripcion.setText("");
                TpDescripcion.setBorder(codigoTP.getBorder());
                TpStatus.setSelected(true);
                break;
            case 2:
                codigoTParqueo.setText("0");
                DescripcionTParqueo.setText("");
                DescripcionTParqueo.setBorder(codigoTParqueo.getBorder());
                cantidad.setText("");
                cantidad.setBorder(codigoTParqueo.getBorder());
                precio.setText("");
                precio.setBorder(codigoTParqueo.getBorder());
                TparqueoStatus.setSelected(true);
                break;
        }
    }
    
    public void buscarRegistros(String texto){
        String query = "";
        query = (jtabpanel==0)?"call p_listarTiposUsuarios('"+texto.trim()+"')":(jtabpanel==1)?"call p_listarTiposPagos('"+texto.trim()+"')":(jtabpanel==2)?"call p_listarTiposParqueos('"+texto.trim()+"')":"";
        mysql cn = new mysql();
        ResultSet rs = cn.ejecutar(query);
        cn.desconectar();
        DefaultTableModel model1 = (DefaultTableModel)((jtabpanel==0)?jtableTiposUsuarios.getModel():(jtabpanel==1)?jtableTiposPagos.getModel():(jtabpanel==2)?jtableTiposParqueos.getModel():null);
        model1.setNumRows(0);
        try {
            while(rs.next()){
                if(jtabpanel==0) model1.addRow(new Object[]{rs.getString("codigo"),rs.getString("descripcion"),rs.getString("estado")});
                if(jtabpanel==1) model1.addRow(new Object[]{rs.getString("codigo"),rs.getString("descripcion"),rs.getString("estado")});
                if(jtabpanel==2) model1.addRow(new Object[]{rs.getString("codigo"),rs.getString("descripcion"),rs.getString("cantidad"),rs.getString("precio"),rs.getString("estado")});
            }
        } catch (SQLException ex) {
            Logger.getLogger(regUsuario.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
