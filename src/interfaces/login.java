    private void b_loginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_b_loginActionPerformed
        String login = t_login.getText().trim();
        char[] pass = t_clave.getPassword();
        String clave = new String(pass);
        if(!("".equals(clave)) && !("".equals(login))){
            String encriptar = Hash.getSha1(clave);
            String query = "call p_login('"+login+"','"+encriptar+"')";
            mysql cn = new mysql();
            ResultSet rs = cn.ejecutar(query);
            try {
                if(rs.next()){
                    if(rs.getInt("existe")>0){                    
                        JOptionPane.showMessageDialog(null,"Bienvenido al Sistema "+rs.getString("login"));
                        setVisible(false);
                        mainform mf = new mainform();
                        mf.setusername(rs.getString("login"),rs.getInt("codigo"));
                        mf.setVisible(true);
                    }else{
                        JOptionPane.showMessageDialog(null, "Credenciales Incorrectos!");
                    }
                }else{
                    JOptionPane.showMessageDialog(null, "Credenciales Incorrectos!");
                }
            } catch (SQLException ex) {
                Logger.getLogger(login.class.getName()).log(Level.SEVERE, null, ex);
            }
        }else{
            JOptionPane.showMessageDialog(null,"Los campos son obligatorios!");
        }
    }//GEN-LAST:event_b_loginActionPerformed

